  // Умный поиск актеров с автодополнением
  static List<String> searchActors(String query) {
    if (query.trim().isEmpty) return [];

    final normalizedQuery = query.toLowerCase().trim();
    final List<String> suggestions = [];

    // Точные совпадения в начале
    for (final actor in actorMovies.keys) {
      final normalizedActor = actor.toLowerCase();

      // Точное совпадение начала
      if (normalizedActor.startsWith(normalizedQuery)) {
        suggestions.add(_capitalizeActorName(actor));
      }
    }

    // Затем частичные совпадения
    for (final actor in actorMovies.keys) {
      final normalizedActor = actor.toLowerCase();
      final actorParts = normalizedActor.split(' ');
      final queryParts = normalizedQuery.split(' ');

      // Проверяем совпадения слов
      bool hasPartialMatch = false;
      for (final queryPart in queryParts) {
        if (queryPart.length >= 2) {
          for (final actorPart in actorParts) {
            if (actorPart.contains(queryPart) ||
                queryPart.contains(actorPart)) {
              hasPartialMatch = true;
              break;
            }
          }
        }
      }
      if (hasPartialMatch) {
        final capitalizedName = _capitalizeActorName(actor);
        if (!suggestions.contains(capitalizedName)) {
          suggestions.add(capitalizedName);
        }
      }
    }

    // Fuzzy search для опечаток
    if (suggestions.length < 5) {
      for (final actor in actorMovies.keys) {
        if (suggestions.contains(_capitalizeActorName(actor))) continue;

        if (_fuzzyMatch(normalizedQuery, actor.toLowerCase())) {
          suggestions.add(_capitalizeActorName(actor));
        }
      }
    }

    return suggestions.take(8).toList();
  }

  // Простой fuzzy search
  static bool _fuzzyMatch(String query, String target) {
    if (query.length < 3) return false;

    // Алгоритм Левенштейна упрощенный
    final maxDistance = (query.length * 0.3).round(); // 30% отличий допустимо

    return _levenshteinDistance(query, target) <= maxDistance;
  }

  // Расстояние Левенштейна
  static int _levenshteinDistance(String s1, String s2) {
    if (s1.isEmpty) return s2.length;
    if (s2.isEmpty) return s1.length;

    final matrix = List.generate(
      s1.length + 1,
      (i) => List.generate(s2.length + 1, (j) => 0),
    );

    for (int i = 0; i <= s1.length; i++) {
      matrix[i][0] = i;
    }
    for (int j = 0; j <= s2.length; j++) {
      matrix[0][j] = j;
    }

    for (int i = 1; i <= s1.length; i++) {
      for (int j = 1; j <= s2.length; j++) {
        final cost = s1[i - 1] == s2[j - 1] ? 0 : 1;
        matrix[i][j] = [
          matrix[i - 1][j] + 1, // deletion
          matrix[i][j - 1] + 1, // insertion
          matrix[i - 1][j - 1] + cost, // substitution
        ].reduce((a, b) => a < b ? a : b);
      }
    }

    return matrix[s1.length][s2.length];
  }

  // Улучшенная проверка актера
  static bool isKnownActor(String name) {
    final normalizedName = name.toLowerCase().trim();

    // Точное совпадение
    if (actorMovies.containsKey(normalizedName)) {
      return true;
    }

    // Частичное совпадение
    for (final actor in actorMovies.keys) {
      if (actor.toLowerCase() == normalizedName) {
        return true;
      }
    }

    return false;
  }

  // Получить точное имя актера
  static String? getExactActorName(String searchName) {
    final normalizedSearch = searchName.toLowerCase().trim();

    for (final actor in actorMovies.keys) {
      if (actor.toLowerCase() == normalizedSearch) {
        return _capitalizeActorName(actor);
      }
    }

    return null;
  }

  // ... остальные методы остаются без изменений
  static List<String> getActorMovies(String actorName) {
    final normalizedName = actorName.toLowerCase().trim();

    // Точное совпадение
    if (actorMovies.containsKey(normalizedName)) {
      return actorMovies[normalizedName]!;
    }

    // Поиск по ключам
    for (final entry in actorMovies.entries) {
      if (entry.key.toLowerCase() == normalizedName) {
        return entry.value;
      }
    }

    return [];
  }

  static List<String> getAllActors() {
    return actorMovies.keys.map((name) => _capitalizeActorName(name)).toList();
  }

  static String _capitalizeActorName(String name) {
    return name
        .split(' ')
        .map(
          (word) => word.isNotEmpty
              ? word[0].toUpperCase() + word.substring(1)
              : word,
        )
        .join(' ');
  }

